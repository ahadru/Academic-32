.MODEL SMALL

.STACK 100H
.DATA
STR DB "MADAM$"
NPMS DB "NPLN$"


.CODE

MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV ES,AX 

MOV AH,9H
LEA DX,STR
INT 21H   
    
STD
